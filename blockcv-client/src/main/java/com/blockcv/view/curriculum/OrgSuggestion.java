package com.blockcv.view.curriculum;

public class OrgSuggestion {
    private final String idOrg;
    private final String nameOrg;

    public OrgSuggestion(String idOrg, String nameOrg) {
        this.idOrg = idOrg;
        this.nameOrg = nameOrg;
    }

    public String getIdOrg() {
        return idOrg;
    }

    public String getNameOrg() {
        return nameOrg;
    }

}
